#include<bits/stdc++.h>
using namespace std;


int main(){
    
    float a,b; cin>>a>>b;
    double c,d; cin>>c>>d;

    printf("A = "); printf("%.6f",a);
    printf(", B = "); printf("%.6f",b);
    cout<<endl;

    printf("C = "); printf("%.6lf",c);
    printf(", D = "); printf("%.6lf",d);
    cout<<endl;

    printf("A = "); printf("%.1f",a);
    printf(", B = "); printf("%.1f",b);
    cout<<endl;

    printf("C = "); printf("%.1lf",c);
    printf(", D = "); printf("%.1lf",d);
    cout<<endl;

    printf("A = "); printf("%.2f",a);
    printf(", B = "); printf("%.2f",b);
    cout<<endl;

    printf("C = "); printf("%.2lf",c);
    printf(", D = "); printf("%.2lf",d);
    cout<<endl;

    printf("A = "); printf("%.3f",a);
    printf(", B = "); printf("%.3f",b);
    cout<<endl;

    printf("C = "); printf("%.3lf",c);
    printf(", D = "); printf("%.3lf",d);
    cout<<endl;

    printf("A = "); printf("%.3E",a);
    printf(", B = "); printf("%.3E",b);
    cout<<endl;

    printf("C = "); printf("%.3E",c);
    printf(", D = "); printf("%.3E",d);
    cout<<endl;


    printf("A = "); printf("%.0f",a);
    printf(", B = "); printf("%.0f",b);
    cout<<endl;

    printf("C = "); printf("%.0lf",c);
    printf(", D = "); printf("%.0lf",d);
    cout<<endl;
    
    return 0;
}

